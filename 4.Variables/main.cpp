#include <iostream>

using namespace std;

int main()
{
    // Variables are just placeholders.
    int x = 7;

    // print 7 + 5. Therefore, print 12.
    cout << x + 5 << endl;

    // Variables can have stupid names.
    int tuna = 6;

    // Print out the value of the variable - 6.
    cout << tuna << endl;

    // Basic arithmetic:
    int a = 4;
    int b = 21;
    int sum = a + b;

    // Print out the sum which should be 4 + 21 = 25.
    cout << sum << endl;

    return 0;
}
